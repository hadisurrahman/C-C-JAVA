package p10071;
import java.util.Scanner;

public class BackToHighSchoolPhysics {

	public static void main(String[] args) 
	{
		int v, t, r;
	    Scanner sc = new Scanner(System.in);
	      while(sc.hasNext())
	      {
	         v = sc.nextInt();
	         t = sc.nextInt();
	         r = 2*v * t;
	         
	         System.out.println(r);

	}

  }
}
