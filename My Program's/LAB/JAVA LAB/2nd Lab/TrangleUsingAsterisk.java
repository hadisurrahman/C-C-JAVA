package print_1_to_10_in_asending_order;

public class TrangleUsingAsterisk
{

	public static void main(String[] args)
	{
		 int i,j;
		    for(i=1;i<=5;i++)
		    {
		        for(j=1;j<=i;j++)
		        {
		           System.out.printf("*");
		        }


		        System.out.printf("\n");

		    }

		
	}

}
